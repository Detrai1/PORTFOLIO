let typingTexts = [];
let count = 0;
let index = 0;
let typingTimeout = null;
let currentLang = localStorage.getItem('lang') || 'ru';

function switchLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-lang]').forEach(el => {
        const key = el.getAttribute('data-lang');
        if (translations[lang][key]) {
            if (el.id === 'hero-desc') {
                el.innerHTML = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });

    typingTexts = [
        translations[lang]["skill-react"],
        translations[lang]["skill-ts"],
        translations[lang]["skill-figma"]
    ];

    count = 0;
    index = 0;
    if (typingTimeout) clearTimeout(typingTimeout);
    type();

    document.getElementById('lang-ru').classList.toggle('active', lang === 'ru');
    document.getElementById('lang-en').classList.toggle('active', lang === 'en');
}

function type() {
    const el = document.getElementById('typing');
    if (!el) return;

    const currentText = typingTexts[count];
    el.textContent = currentText.slice(0, ++index);

    if (index === currentText.length) {
        count = (count + 1) % typingTexts.length;
        index = 0;
        typingTimeout = setTimeout(type, 2000);
    } else {
        typingTimeout = setTimeout(type, 70);
    }
}

window.addEventListener('load', () => {
    switchLanguage(currentLang);
});
